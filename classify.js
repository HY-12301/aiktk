// 归类函数（按科目隔离）
function classify(subj, seg) {
  if (!seg || seg === 'other' || seg === '其他') return '其他';  // 垃圾 sentinel
  const chs = TREE[subj] || [];
  if (chs.includes(seg)) return seg;            // 精确
  const km = KEYMAP[subj] || [];
  for (const [ch, kws] of km) {
    for (const kw of kws) {
      if (kw.length >= 2 && seg.includes(kw)) return ch;
    }
  }
  const sm = SUPP[subj] || [];
  for (const [ch, kws] of sm) {
    for (const kw of kws) {
      if (kw.length >= 2 && seg.includes(kw)) return ch;
    }
  }
  const s2 = SUPP2[subj] || [];
  for (const [ch, kws] of s2) {
    for (const kw of kws) {
      if (kw.length >= 2 && seg.includes(kw)) return ch;
    }
  }
  const s3 = SUPP3[subj] || [];
  for (const [ch, kws] of s3) {
    for (const kw of kws) {
      if (kw.length >= 2 && seg.includes(kw)) return ch;
    }
  }
  const s4 = SUPP4[subj] || [];
  for (const [ch, kws] of s4) {
    for (const kw of kws) {
      if (kw.length >= 2 && seg.includes(kw)) return ch;
    }
  }
  // 兜底：seg 含某章名（>=3字）且 seg 不长于章名+12
  for (const ch of chs) {
    if (ch.length >= 3 && seg.includes(ch) && seg.length <= ch.length + 12) return ch;
  }
  return null;
}

function stripSubject(topic, subj) {
  let t = String(topic || '').trim();
  if (subj && t.indexOf(subj + '.') === 0) t = t.slice(subj.length + 1).trim();
  return t.split(/[；;，,。\n]/)[0].trim();
}

// 收集唯一 (subj, seg)
const uniq = {};
for (const q of DATA) {
  const subj = q.lawName || '';
  const seg = stripSubject(q.topic, subj);
  const key = subj + '\u0000' + (seg || '');
  if (!uniq[key]) uniq[key] = { subj, seg: seg || '', count: 0, sample: '' };
  uniq[key].count++;
  if (!uniq[key].sample && q.stem) uniq[key].sample = String(q.stem).slice(0, 50);
}

// 分类
const map = {};
const residual = {};
let matched = 0, unmatched = 0;
const perSubj = {};
for (const key in uniq) {
  const { subj, seg, count, sample } = uniq[key];
  if (!perSubj[subj]) perSubj[subj] = { total: 0, matched: 0, unmatched: 0 };
  perSubj[subj].total++;
  const ch = seg ? classify(subj, seg) : null;
  if (ch) {
    map[key] = ch; perSubj[subj].matched++; matched++;
  } else {
    if (!residual[subj]) residual[subj] = [];
    residual[subj].push({ seg, count, sample });
    perSubj[subj].unmatched++; unmatched++;
  }
}

// 输出
