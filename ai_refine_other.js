function examPoints(res){
  let r=String(res||'').replace(/\s+/g,'');
  let m=r.match(/本题(?:主要)?(?:考查|考察|考核)([^。]{2,60})/);
  if(!m) return [];
  return m[1].split(/[；;，,、]/).map(function(s){
    return s.replace(/^(以及|以及|和|与|及|以及)/,'').replace(/(相关|问题|规定|制度|的认定|的成立|知识点|等|的|中|原则|内容|效力|的法律适用|案件的撤诉与审理规定|的撤诉与审理|的确定|的法律地位|的具体要求|问题|中的具体处理)$/,'');
  }).filter(function(s){return s.length>=2&&s.length<=20;});
}

