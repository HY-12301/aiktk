function classify(q){
  for(const R of RULES){
    if(R.law!==q.lawName||R.topic!==q.topic) continue;
    const p=examPoint(q.questionResolution);
    const hay=p||String(q.content||'');
    for(const rule of R.rules){
      if(rule.t.test(hay)) return rule.sub;
    }
  }
  return null;
}

// 应用
