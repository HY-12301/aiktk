function examPoint(res){
  let r=String(res||'').replace(/\s+/g,'');
  let m=r.match(/本题(?:主要)?(?:考查|考察|考核)([^。，,；;、]{2,18})/);
  if(m) return m[1].replace(/^的/,'')
    .replace(/(相关|问题|规定|制度|的认定|的成立|知识点|等|的|中|原则|内容)$/,'')
    .trim()||m[1];
  return null;
}

